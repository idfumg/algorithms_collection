int rec(vvi& mat, int i, int j) {
    if (j >= mat[0].size() or i >= mat.size()) return 0;
    const auto a = rec(mat, i + 1, j);
    const auto b = rec(mat, i + 1, j + 1);
    return max(a, b) + mat[i][j];
}

int rec(vvi& mat) {
    return rec(mat, 0, 0);
}

int choose(vvi& mat, int i, int j) {
    const auto m = mat.size();
    const auto n = mat[0].size();
    if (i >= m or j >= n or i < 0 or j < 0 or j > i + 1) return 0;
    return mat[i][j];
}

int tab(vvi& mat) { // with dp state optimized
    vvi dp(2, vi(mat[0].size()));
    int maxi = INT_MIN;
    for (int i = 0; i < mat.size(); ++i) {
        int idx = i & 1;
        for (int j = 0; j <= i and j <= i; ++j) {
            const auto a = choose(dp, 1 - idx, j);
            const auto b = choose(dp, 1 - idx, j - 1);
            dp[idx][j] = max(a, b) + mat[i][j];
            maxi = max(maxi, dp[idx][j]);
        }
    }
    return maxi;
}

