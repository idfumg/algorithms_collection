int divide_by_power(int n, int power) {
    while (n % power == 0) n /= power;
    return n;
}

bool is_ugly(int n) {
    n = divide_by_power(n, 2);
    n = divide_by_power(n, 3);
    n = divide_by_power(n, 5);
    return n == 1;
}

int naive(int n) {
    int count = 1, i = 2;
    for (; count < n; ++i) {
        if (is_ugly(i)) {
            ++count;
        }
    }
    return i - 1;
}

// 1, 2, 3, 4, 5, 6, 8, 9, 10, 12, 15, …
// (1) 1×2, 2×2, 3×2, 4×2, 5×2, …
// (2) 1×3, 2×3, 3×3, 4×3, 5×3, …
// (3) 1×5, 2×5, 3×5, 4×5, 5×5, …

int fast(int n) {
    vector<int> dp(n + 2);
    int i2 = 0, i3 = 0, i5 = 0;
    int next_multiple_of_2 = 2;
    int next_multiple_of_3 = 3;
    int next_multiple_of_5 = 5;
    dp[0] = 1;
    int next_ugly = 1;
    for (int i = 1; i < n; ++i) {
        next_ugly = min({next_multiple_of_2,
                         next_multiple_of_3,
                         next_multiple_of_5});
        dp[i] = next_ugly;

        if (next_ugly == next_multiple_of_2) {
            ++i2;
            next_multiple_of_2 = dp[i2] * 2;
        }
        if (next_ugly == next_multiple_of_3) {
            ++i3;
            next_multiple_of_3 = dp[i3] * 3;
        }
        if (next_ugly == next_multiple_of_5) {
            ++i5;
            next_multiple_of_5 = dp[i5] * 5;
        }
    }
    return next_ugly;
}

