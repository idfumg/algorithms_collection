#include "../../template.hpp"

void Bin(size_t num) {
    static constexpr int n = sizeof(num) * 8 - 1;
    for (size_t i = 1ULL << n; i > 0; i /= 2) {
        if (num & i) {
            cout << 1;
        }
        else {
            cout << 0;
        }
    }
    cout << '\n';
}

